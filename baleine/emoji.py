

def parse_emojis(text):
    items = text.split(':')
    result = []

    skip_next = True
    for idx, item in enumerate(items):
        if skip_next:
            result.append(item)
            skip_next = False
            continue
        emoji = EMOJI_TABLE.get(item)
        if emoji is None:
            result.extend((':', item))
        else:
            result.append(emoji)
            skip_next = True

    return ''.join(result)

